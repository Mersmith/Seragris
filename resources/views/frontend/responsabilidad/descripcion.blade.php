        <p style="text-align: justify;">Seragris tiene como
            misión el contribuir con el
            cuidado de la salud humana, la salud animal y con la protección del ambiente, a través de la gestión
            integral y responsable de los envases vacíos de agroquímicos, involucrando y comprometiendo a todos los
            actores de la cadena agroalimentaria. Como Objetivo principal tenemos la gestión de los envases vacíos de
            plaguicidas abandonados en el campo, que puede generar contaminación por residuos de estas sustancias en los
            suelos y las fuentes de agua subterráneas, contaminando el ambiente y ocasionando daños a la salud de los
            seres humanos como de los animales. En ese sentido, Campo Limpio pone a disposición su red de centros de
            acopio para recepción de envases, en constante proceso de expansión. <br><br>CAMPO LIMPIO es un programa de
            destino
            final de los envases usados de PQUAs, desarrollando y operando el sistema de eliminación
            adecuadamente.
            <br><br>
            Cuenta con 02 plantas de reprocesamiento de envases plásticos y 10 puntos de acopio
            intermedio, autorizadas por la DIGESA, DGAAA y las respectivas municipalidades; con capacidad para procesar
            más de 300 toneladas anuales en conjunto.
